public class Hello6 {
	public static void main(String[] args) {
		byte x = 23;
		System.out.println(x);
		byte y = 0b00010111; // 2 進位
		System.out.println(y);
		System.out.println(1_000_000);
		System.out.println(100_0000);
		System.out.println(1_0_0_0_0_0_0);
		System.out.println(2_0368_5477_5808L);
		System.out.println(017); // 8 進位
		System.out.println(0xab); // 16 進位
	}
}